#include <stdio.h>

int main() {
    int a,b,c,
        d,e,f,
        g,h,i;
    scanf("%d %d %d %d %d %d %d %d %d", &a, &b, &c, &d, &e, &f, &g, &h, &i);
    // Matrix A
    int A[3][3] = {a, b, c, d, e, f, g, h, i};

    scanf("%d %d %d %d %d %d %d %d %d", &a, &b, &c, &d, &e, &f, &g, &h, &i);
    // Matrix B
    int B[3][3] = {a, b, c, d, e, f, g, h, i};

    // Matrix C hasil perkalian A dan B
    int C[3][3] = {
        A[0][0] * B[0][0] + A[0][1] * B[1][0] + A[0][2] * B[2][0], 
        A[0][0] * B[0][1] + A[0][1] * B[1][1] + A[0][2] * B[2][1], 
        A[0][0] * B[0][2] + A[0][1] * B[1][2] + A[0][2] * B[2][2], 
        A[1][0] * B[0][0] + A[1][1] * B[1][0] + A[1][2] * B[2][0], 
        A[1][0] * B[0][1] + A[1][1] * B[1][1] + A[1][2] * B[2][1], 
        A[1][0] * B[0][2] + A[1][1] * B[1][2] + A[1][2] * B[2][2], 
        A[2][0] * B[0][0] + A[2][1] * B[1][0] + A[2][2] * B[2][0], 
        A[2][0] * B[0][1] + A[2][1] * B[1][1] + A[2][2] * B[2][1], 
        A[2][0] * B[0][2] + A[2][1] * B[1][2] + A[2][2] * B[2][2]
    };

    // Matrix D hasil perkalian transpose B dan transpose A
    int D[3][3] = {
        B[0][0] * A[0][0] + B[1][0] * A[1][0] + B[2][0] * A[2][0], 
        B[0][0] * A[0][1] + B[1][0] * A[1][1] + B[2][0] * A[2][1], 
        B[0][0] * A[0][2] + B[1][0] * A[1][2] + B[2][0] * A[2][2], 
        B[0][1] * A[0][0] + B[1][1] * A[1][0] + B[2][1] * A[2][0], 
        B[0][1] * A[0][1] + B[1][1] * A[1][1] + B[2][1] * A[2][1],
        B[0][1] * A[0][2] + B[1][1] * A[1][2] + B[2][1] * A[2][2], 
        B[0][2] * A[0][0] + B[1][2] * A[1][0] + B[2][2] * A[2][0], 
        B[0][2] * A[0][1] + B[1][2] * A[1][1] + B[2][2] * A[2][1], 
        B[0][2] * A[0][2] + B[1][2] * A[1][2] + B[2][2] * A[2][2]
};

// Matrix E hasil XOR matrix C dan D
int E[3][3] = {
    C[0][0] ^ D[0][0], C[0][1] ^ D[0][1], C[0][2] ^ D[0][2],
    C[1][0] ^ D[1][0], C[1][1] ^ D[1][1], C[1][2] ^ D[1][2],
    C[2][0] ^ D[2][0], C[2][1] ^ D[2][1], C[2][2] ^ D[2][2]
};

for(int i = 0; i < 3; i++) {
    for(int j = 0; j < 3; j++) {
        printf("%d ", E[i][j]);
    }
    printf("\n");
}

return 0;
}
